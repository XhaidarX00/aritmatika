package aritmatika

// penjumlahan
func AritmatikaPenjumlahan(a int, b int) int {
	return a + b
}

// perngurangan
func AritmatikaPengurangan(a int, b int) int {
	return a - b
}

// perkalian
func AritmatikaPerkalian(a int, b int) int {
	return a * b
}

// perngurangan
func AritmatikaPembagian(a int, b int) int {
	return a / b
}

// sisa bagi
func AritmatikaSisaBagi(a int, b int) int {
	return a % b
}
